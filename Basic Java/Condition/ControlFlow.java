public class ControlFlow {
   public static void main(String[] args) {
      int age = 24;
      if (age > 24) {
         System.out.println("you are old");
      } else if (age <= 24 && age > 18) {
         System.out.println("you are young");
      }

      else {
         System.out.println("you are child");
      }
   }
}
